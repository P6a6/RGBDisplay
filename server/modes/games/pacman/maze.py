def make_maze() -> list[list[int]]:
    return [row[:] for row in _M]

def count_dots(maze: list[list[int]]) -> int:
    return sum(cell in (DOT, PELLET) for row in maze for cell in row)

def is_passable(maze: list[list[int]], col: int, row: int,
                ghost_mode: str = "normal") -> bool:
    """
    ghost_mode: "normal" | "house" | "eaten"
    "house"/"eaten" modes may pass through DOOR tiles.
    Pac-Man always uses "normal" (cannot enter DOOR or ghost-only areas).
    """
    # Tunnel wrap on row 14
    if row == 14:
        col = col % MAZE_W
    if col < 0 or col >= MAZE_W or row < 0 or row >= MAZE_H:
        return False
    cell = maze[row][col]
    if cell == WALL:
        return False
    if cell == DOOR:
        return ghost_mode in ("house", "eaten")
    return True

MAZE_W = 28
MAZE_H = 31
TUNNEL_ROW = 14
